    <h1>Privacy Policy</h1>
    <p class="updated">Neon Rail Dash (Lane Runner) &middot; Last updated: July 12, 2026</p>

    <p>
        Neon Rail Dash ("the game") is developed by Daniel Remensperger. This policy explains
        what data the game processes when you play — on the website
        (lanerunner.on-forge.com) or in the Android app.
    </p>

    <h2>Data we collect</h2>
    <ul>
        <li><strong>Account data (optional):</strong> If you register, we store your name, e-mail
            address and a hashed password. Playing as a guest requires no account.</li>
        <li><strong>Gameplay data:</strong> Scores, run distances, speeds, run durations, unlocked
            stages, purchased skins and coin balances — used for leaderboards, progress and
            fair-play (anti-cheat) checks.</li>
        <li><strong>Device identifier:</strong> A random identifier generated on your device (not
            derived from personal data). It is used for fair-play enforcement and to link
            ad events and bug reports without requiring an account.</li>
        <li><strong>Bug reports:</strong> If you submit a bug report, we store the text you enter
            together with basic technical context.</li>
        <li><strong>Local storage:</strong> Settings (volume, controls, orientation), progress
            counters and cached game assets are stored locally on your device and are not
            transmitted except as described above.</li>
    </ul>

    <h2>Advertising (Android app only)</h2>
    <p>
        The Android app shows ads via <strong>Google AdMob</strong>. AdMob may process your
        advertising ID and device information to serve and measure ads. Where required
        (e.g. EEA/UK), you are asked for consent before personalized ads are shown, and you can
        choose non-personalized ads. See
        <a href="https://policies.google.com/privacy" rel="noopener">Google's Privacy Policy</a>
        and <a href="https://support.google.com/admob/answer/6128543" rel="noopener">how Google uses
        data in AdMob apps</a>. The website and PWA show no ads.
    </p>

    <h2>What we do not do</h2>
    <ul>
        <li>We do not sell your data.</li>
        <li>We do not collect precise location, contacts, photos or other sensitive device data.</li>
        <li>We do not send marketing e-mails.</li>
    </ul>

    <h2>Data retention &amp; deletion</h2>
    <p>
        Account and gameplay data are kept while your account exists. You can delete your account
        (including profile and gameplay records) at any time on your profile page, or request
        deletion by e-mail. Guest gameplay data is only linked to the random device identifier.
    </p>

    <h2>Legal basis</h2>
    <p>
        We process gameplay and device data based on our legitimate interest in providing a
        functioning, fair online game (Art. 6(1)(f) GDPR), account data to fulfil the service you
        request (Art. 6(1)(b) GDPR), and personalized advertising only with your consent
        (Art. 6(1)(a) GDPR).
    </p>

